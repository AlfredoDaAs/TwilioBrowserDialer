<script>
import axios from "./axios";
import { initFirebaseApp } from './firebase'

export default {
  name: "app",
  methods: {
    async getToken() {
      const result = await axios.get("/token");

      if (result.data) {
        const token = result.data.token;
        this.$store.dispatch("setToken", token);
      }
    },
  },
  created() {
        initFirebaseApp();
        this.getToken();
  },
};
</script>

<template>
  <div id="app">
    <router-view />
  </div>
</template>

